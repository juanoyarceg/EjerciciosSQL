
CREATE DATABASE IF NOT EXISTS data_stock;

USE data_stock;

DROP TABLE IF EXISTS ingresos_retiros;
DROP TABLE IF EXISTS bodega_sector;
DROP TABLE IF EXISTS bodega;
DROP TABLE IF EXISTS productos;
DROP TABLE IF EXISTS clientes;


CREATE TABLE bodega (
  num INT(11) NOT NULL AUTO_INCREMENT,
  codigo VARCHAR(100) DEFAULT NULL,
  detalle VARCHAR(500) DEFAULT NULL,
  direccion VARCHAR(255) DEFAULT NULL,
  PRIMARY KEY (num)
);


CREATE TABLE bodega_sector (
  num INT(11) NOT NULL AUTO_INCREMENT,
  codigo VARCHAR(100) DEFAULT NULL,
  detalle VARCHAR(500) DEFAULT NULL,
  bodega INT(11) DEFAULT NULL,
  PRIMARY KEY (num),
  FOREIGN KEY (bodega) REFERENCES bodega(num)
);


CREATE TABLE clientes (
  num INT(11) NOT NULL AUTO_INCREMENT,
  rut VARCHAR(10) DEFAULT NULL,
  dig VARCHAR(1) DEFAULT NULL,
  razonsocial VARCHAR(255) DEFAULT NULL,
  direccion VARCHAR(255) DEFAULT NULL,
  fono INT(11) DEFAULT NULL,
  PRIMARY KEY (num)
);


CREATE TABLE productos (
  num INT(11) NOT NULL AUTO_INCREMENT,
  codigo VARCHAR(100) DEFAULT NULL,
  detalle VARCHAR(500) DEFAULT NULL,
  volumen DECIMAL(5,2) DEFAULT NULL,
  cliente INT(11) DEFAULT NULL,
  PRIMARY KEY (num),
  FOREIGN KEY(cliente) REFERENCES clientes(num)
);


CREATE TABLE ingresos_retiros (
  num INT(11) NOT NULL AUTO_INCREMENT,
  producto INT(11) DEFAULT NULL,
  sector INT(11) DEFAULT NULL,
  cantidad INT(11) DEFAULT NULL,
  fecha DATE DEFAULT NULL,
  tipo INT(11) DEFAULT NULL,
  PRIMARY KEY (num), 
  FOREIGN KEY(producto) REFERENCES productos(num),
  FOREIGN KEY(sector) REFERENCES bodega_sector(num)
);


DROP TABLE IF EXISTS stock;
CREATE TABLE stock (
  sector INT(11) NOT NULL,
  producto INT(11) NOT NULL,
  cantidad INT(11) DEFAULT NULL,
  PRIMARY KEY (sector,producto)
);


